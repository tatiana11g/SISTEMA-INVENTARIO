    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        
        String usuario = txtUsuario.getText();
        String clave = new String(txtClave.getPassword());
        DUsuarios dts = new DUsuarios();
        LUsuarios fn = new LUsuarios();
        dts.setUsuario(usuario);
        dts.setClave(clave);
        String perfil = fn.ValidarLogin(dts);
        if(perfil.equals("")){
            JOptionPane.showMessageDialog(rootPane, "No se encontro el usuario, comunicate con el adminitrador");
            txtUsuario.setText("");
            txtClave.setText("");
            txtUsuario.requestFocusInWindow();
        }else{
            MDIPrincipal miPrincipal = new MDIPrincipal();
            miPrincipal.setDatos(usuario,perfil);
            miPrincipal.setExtendedState(JFrame.MAXIMIZED_BOTH);
            miPrincipal.setVisible(true);
            this.dispose();
        }
        

    }//GEN-LAST:event_btnIngresarActionPerformed

