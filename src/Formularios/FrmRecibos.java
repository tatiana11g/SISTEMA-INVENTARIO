    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String msgfinal = "";
        if (txtRecibe.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Campo persona recibe obligatorio", "Validaci贸n", JOptionPane.WARNING_MESSAGE);
            txtRecibe.requestFocusInWindow();
            return;
        }

        if (txtFecha.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Campo fecha obligatorio", "Validaci贸n", JOptionPane.WARNING_MESSAGE);
            txtFecha.requestFocusInWindow();
            return;
        }

        LRecibos fn = new LRecibos();
        DRecibos dts = new DRecibos();

        Calendar cal;

        cal = txtFecha.getCalendar();

        int aa, mm, dd;

        aa = cal.get(Calendar.YEAR) - 1900;
        mm = cal.get(Calendar.MONTH);
        dd = cal.get(Calendar.DAY_OF_MONTH);

        dts.setFechaRecibo(new Date(aa, mm, dd));
        dts.setPersonaEntrega(lblPersonaE.getText());
        dts.setPersonaRecibe(txtRecibe.getText());

        int idrec = fn.insertarRecibos(dts);
        if (idrec > 0) {
            int filas = tblRecibos.getRowCount();
            for (int i = 0; i < filas; i++) {

                String idprod = tblRecibos.getValueAt(i, 0).toString();
                int idlinea = Integer.parseInt(tblRecibos.getValueAt(i, 7).toString());
                int cantidad = Integer.parseInt(tblRecibos.getValueAt(i, 2).toString());
                double total = Double.parseDouble(tblRecibos.getValueAt(i, 6).toString());
                
                LDetalleRecibos fundf = new LDetalleRecibos();
                DDetalleRecibos dtsf = new DDetalleRecibos();
                dtsf.setRecibosId(idrec);
                //ertar_datellerecib(in recibid int, in prodid varchar(45), in idlin int, in cant int, in tot decimal(18,2))
                dtsf.setProductosId(idprod);
                dtsf.setLineasId(idlinea);
                dtsf.setCantidad(cantidad);
                dtsf.setTotal(total);
                String mensaje = fundf.registrarDetalleRecibos(dtsf);
                
                  if (mensaje.equals("si")) {
                    LAlmacen funAl = new LAlmacen();
                    DAlmacen dtsp = new DAlmacen();

                    dtsp.setIdAlmacen(idprod);
                    dtsp.setStock(cantidad);
                  

                    msgfinal = funAl.disminuirStock(dtsp);

                }

            }
            JOptionPane.showMessageDialog(rootPane, msgfinal, "Informaci贸n", JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocurio un problema al insertar", "Informaci贸n", JOptionPane.ERROR_MESSAGE);
        }
        limpiar();
        habilitar(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

